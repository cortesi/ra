//! ra: Research Assistant
//!
//! A knowledge management system for autonomous coding and writing agents.
//!
//! ra provides structured access to curated knowledge bases for AI agents. Users maintain
//! collections of markdown documents—project-specific and global—that provide context for
//! agent tasks. Because the full corpus may exceed practical context limits, ra indexes
//! these documents and exposes search tools that agents use to retrieve relevant
//! context on demand.

